package com.hcl.java.day1HandsOnExcercise;

public class SumMulDivRemainder {
	public static void main(String[] args) {
		int inputFirstNumber  = 20; 
		int inputSecondNumber = 4;
		
		System.out.println("Add: "+ inputFirstNumber+inputSecondNumber);
		System.out.println("Substraction: "+ (inputFirstNumber-inputSecondNumber));
		System.out.println("Multiplication: "+ inputFirstNumber*inputSecondNumber);
		System.out.println("Division: "+ (inputFirstNumber/inputSecondNumber));
		System.out.println("Reaminder: "+ (inputFirstNumber%inputSecondNumber));

	}

}
